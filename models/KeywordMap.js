// ─── Search query se matched DB categories aur subcategories nikalo ──────────
export const getDbCategoriesFromSearch = (searchQuery) => {
  if (!searchQuery) return { dbCategories: [], dbSubCategories: [] };

  const q = searchQuery.toLowerCase().trim();
  const matchedDbCategories = new Set();
  const matchedDbSubCategories = new Set();

  for (const [catName, data] of Object.entries(CATEGORY_KEYWORD_MAP)) {
    let matched = false;

    // 1. Category name se match
    if (
      catName.toLowerCase().includes(q) ||
      q.includes(catName.toLowerCase())
    ) {
      matched = true;
    }

    // 2. Keywords se match
    if (!matched) {
      matched = data.keywords.some(
        (kw) =>
          kw.toLowerCase().includes(q) || q.includes(kw.toLowerCase())
      );
    }

    // 3. DB category/subcategory names se match
    if (!matched) {
      matched =
        data.dbCategories.some((c) =>
          c.toLowerCase().includes(q) || q.includes(c.toLowerCase())
        ) ||
        data.dbSubCategories.some((s) =>
          s.toLowerCase().includes(q) || q.includes(s.toLowerCase())
        );
    }

    if (matched) {
      data.dbCategories.forEach((c) => matchedDbCategories.add(c));
      data.dbSubCategories.forEach((s) => matchedDbSubCategories.add(s));
    }
  }

  return {
    dbCategories: [...matchedDbCategories],
    dbSubCategories: [...matchedDbSubCategories],
  };
};